package pp_fp05_ex2;

public class Track {
    
    public int numeroFaixa;
    
    public String nomeFaixa;
    
    public int duracao;
    
    public String nomeAutor;
    
    public Track(int tempNumero, String tempNome, int tempDuracao, String tempAutor) {
        numeroFaixa = tempNumero;
        nomeFaixa = tempNome;
        duracao = tempDuracao;
        nomeAutor = tempAutor;
    }
}